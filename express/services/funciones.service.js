export function sumar (numero1, numero2){
    let suma = numero1 + numero2;
    return suma;
}

export function resta (numero1, numero2){
    let resta = numero1 - numero2;
    return resta;
}

export function multiplica (numero1, numero2){
    let multiplica = numero1 * numero2;
    return multiplica;
}

export function divide (numero1, numero2){
    let divide = numero1 / numero2;
    return divide;
}